var a = prompt("Enter Your Percentage");

if (a < 50) {
  alert("You are fail");
} else if (a >= 50 && a < 60) {
  alert("Your Grade is D");
} else if (a >= 60 && a < 70) {
  alert("Your Grade is C");
} else if (a >= 70 && a < 80) {
  alert("Your Grade is B");
} else if (a >= 80 && a < 90) {
  alert("Your Grade is A");
} else if (a >= 90 && a < 100) {
  alert("Your Grade is A+");
} else {
  alert("Try Again");
}

